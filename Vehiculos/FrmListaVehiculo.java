    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
        String matricula = txtMatricula.getText();
    String modelo = txtModelo.getText();
    String marca = txtMarca.getText();
    int anio;
    
    try {
        anio = Integer.parseInt(txtAnio.getText());
        
        Vehiculo vehiculo = new Vehiculo(matricula, modelo, marca, anio);
        
        listaVehiculos.agregarVehiculo(vehiculo);
        
        txtResultados.setText("VehÃ­culo agregado: " + vehiculo.toString());
        
    } catch (NumberFormatException ex) {
        txtResultados.setText("Error: El aÃ±o de fabricaciÃ³n debe ser un nÃºmero entero.");
    }
    }//GEN-LAST:event_btnAgregarActionPerformed

